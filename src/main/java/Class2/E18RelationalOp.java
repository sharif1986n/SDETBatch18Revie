package Class2;

public class E18RelationalOp {
    public static void main(String[] args) {
        int num1=10;
        int num2=20;
        System.out.println(num1>num2);//False
        System.out.println(num1<num2);//True

        //num1 is equal to (num1==num2) to num2
        System.out.println(num1==num2);//False


        //num1 is not equal to (num1!==num2) to num2
        System.out.println(num1!=num2);//True
    }
}
